# Design Ref: §5 (Phase별 상세 설계) — 각 파이프라인 Phase에서 사용하는 GPT-4 프롬프트 템플릿

PROMPT_OVERVIEW = """\
당신은 소스코드 분석 전문가입니다.
다음 소스코드(Python/Java/C/C++ 등)를 분석하여 두 문서를 작성하세요.

[소스코드]
{file_contents}

반드시 아래 형식 그대로 출력하세요 (### 헤더 포함):

### PRD.md
(이 프로젝트가 해결하는 문제, 주요 기능, 사용자 시나리오를 마크다운으로 작성)

### DESIGN.md
(전체 아키텍처, 주요 컴포넌트, 데이터 흐름을 마크다운으로 작성)
"""

PROMPT_COMMENT = """\
다음은 프로젝트 개요입니다.

[PRD]
{prd_content}

[DESIGN]
{design_content}

위 맥락을 참고하여, 아래 클래스에 한글 주석을 추가하세요.

규칙:
- 클래스 정의 바로 아래: 클래스 역할을 1~3줄 docstring으로 설명
- 각 메서드 정의 바로 아래: 목적, Args, Returns 형식의 docstring
- 메서드 내부 주요 로직: 인라인 주석 (# 설명)
- 자명한 코드(변수 할당 등)에는 주석 생략
- 원본 로직은 절대 수정하지 말 것 (주석만 추가)

[원본 클래스 소스]
{class_source}

[주석이 달린 클래스 소스코드만 출력하세요. 다른 설명 없이 코드만.]

[클래스 목적 한 줄]
(아래에 "목적:" 접두어 없이 클래스의 핵심 역할을 한 문장으로 작성)
"""

PROMPT_GRAPH_NODES = """\
다음은 소스코드 분석 결과입니다.

[analysis.md]
{analysis_content}

[DESIGN.md]
{design_content}

이 중에서 의미론적으로 핵심이 되는 컴포넌트(Node)를 추출하세요.

기준:
- 너무 세부적인 유틸리티 클래스는 제외
- 시스템의 주요 책임을 담당하는 클래스 위주로 선택
- 전체 클래스의 30~70% 수준으로 선별

JSON 배열만 출력하세요 (설명 없이):
[{{"id": "클래스명", "label": "한글 레이블", "purpose": "한 줄 목적 설명"}}]
"""

PROMPT_GRAPH_EDGES = """\
다음은 핵심 Node 목록과 클래스 의존성 정보입니다.

[Node 목록]
{nodes_json}

[의존성 그래프]
{dependency_graph}

Node 간의 의미적 연결(Edge)을 추출하세요.

기준:
- 구조적 의존성이 아닌 의미론적 관계를 설명
- 관계 레이블은 한글로, 동사구 형태로 작성 (예: "사용자 정보를 조회하여 인증 처리")
- 단순 상속 관계는 제외하고 기능적 협력 관계만 포함

JSON 배열만 출력하세요 (설명 없이):
[{{"source": "NodeId1", "target": "NodeId2", "relation": "한글 관계 설명"}}]
"""
